    private void popuniSprave() {
        KlijentskiZahtev kz = new KlijentskiZahtev();
        kz.setOperacija(operacije.Operacije.VRATI_LISTU_SPRAVA);
        kz.setParametar(new Sprava());
        komunikacija.KomunikacijaSaServerom.getInstanca().posaljiZahtev(kz);
        ServerskiOdgovor so = komunikacija.KomunikacijaSaServerom.getInstanca().primiOdgovor();
        
        ArrayList<Sprava> sprave = (ArrayList<Sprava>) so.getOdgovor();
        cmbSprave.removeAllItems();
        for (Sprava sprava : sprave) {
            cmbSprave.addItem(sprava);
            
        }
    }
