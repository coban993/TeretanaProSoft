    private void btnDodajPlanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajPlanActionPerformed
        
        try {
            if(txtBrojSerija.getText().isEmpty() || txtBrojPonavljanja.getText().isEmpty() || txtDuzinaOdmora.getText().isEmpty()
                || txtOpisVezbe.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Sva polja su obavezna");
            return;
            }
            String brojSerija = txtBrojSerija.getText();
            String brojPonavljanja = txtBrojPonavljanja.getText();
            String duzinaOdmora = txtDuzinaOdmora.getText();
            String opisVezbe = txtOpisVezbe.getText(); 
            
            int brojPonavljanja1 = Integer.parseInt(brojPonavljanja);
            int brojSerija1 = Integer.parseInt(brojSerija);
            int duzinaOdmora1 = Integer.parseInt(duzinaOdmora);
            
            Sprava sprava = (Sprava) cmbSprave.getSelectedItem();
        
            ModelTabeleStavke mts = (ModelTabeleStavke) tblStavka.getModel();
        
            if(mts.vecPostoji(sprava)){
                JOptionPane.showMessageDialog(this,"Ova stavka vec postoji u tabeli");
                return;
            }
        
            StavkaPlanaTreninga spt = new StavkaPlanaTreninga(new PlanTreninga(), -1, brojSerija1,
                brojPonavljanja1, duzinaOdmora1, opisVezbe, sprava);
            
            mts.dodajStavku(spt);
            
        }catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Niste uneli ispravne podatke, proverite da li ste u pollja broj serija,"
                    + " broj ponavljanja i duzina odmora uneli broj!");
        }   
    }//GEN-LAST:event_btnDodajPlanActionPerformed

