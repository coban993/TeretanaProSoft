    private void btnKreirajTreningActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKreirajTreningActionPerformed
        try {
            ModelTabeleStavke mts = (ModelTabeleStavke) tblStavka.getModel();
            if(mts.getLista().isEmpty()){
                JOptionPane.showMessageDialog(this, "Morate uneti bar jednu stavku plana treninga");
                return;
            }
            if(txtNazivTreninga.getText().isEmpty() || txtSvrha.getText().isEmpty() || txtDuzinaTrajanja.getText().isEmpty() 
                    || txtStatus.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "Sva polja ispod tabele su obavezna da bi kreirali plan treninga");
                return;
            }
            String nazivTreninga = txtNazivTreninga.getText();
            String svrhaTreninga = txtSvrha.getText();
            String duzinaTrajanja = txtDuzinaTrajanja.getText();
            String status = txtStatus.getText();
            
            int duzinaTrajanja1 = Integer.parseInt(duzinaTrajanja);
            TrenutniPlanTreninga trenutni = new TrenutniPlanTreninga(null, null, status);
            
            PlanTreninga planTreninga = new PlanTreninga(0, nazivTreninga, svrhaTreninga,
                    duzinaTrajanja1,mts.getLista(),vezbac,trenutni);
            
            
            KlijentskiZahtev kz = new KlijentskiZahtev(operacije.Operacije.KREIRANJE_PLANA_TRENINGA, planTreninga);
            komunikacija.KomunikacijaSaServerom.getInstanca().posaljiZahtev(kz);
            ServerskiOdgovor so = komunikacija.KomunikacijaSaServerom.getInstanca().primiOdgovor();
            
            
            if(so.isUspesno()){
                JOptionPane.showMessageDialog(this, so.getPoruka());
            }else{
                JOptionPane.showMessageDialog(this, so.getPoruka(),"GRESKA!",JOptionPane.ERROR_MESSAGE);
            }
            
            this.dispose();
            
        }catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Niste uneli ispravne podatke, polje duzina trajanja mora btit broj");
        }       
        
    }//GEN-LAST:event_btnKreirajTreningActionPerformed

