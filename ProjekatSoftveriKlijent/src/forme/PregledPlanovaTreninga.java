    private void btnDetaljiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDetaljiActionPerformed
        int selectedRow = tblTrening.getSelectedRow();
        if(selectedRow == -1){
            JOptionPane.showMessageDialog(this, "Morate izabrati neki plan treninga");
            return;
        }
        
        modeli.ModelTabelePlanovi mtpt = (modeli.ModelTabelePlanovi) tblTrening.getModel();
        PlanTreninga pt = mtpt.vratiPlanTreninga(selectedRow);
        pt.setTreningID(vratiPlanTreinigaID(pt));
        
        
        if(pt.getTreningID() != 0){
            AzururanjePlanaTreninga az = new AzururanjePlanaTreninga();
            az.postaviVrednost(pt);
            az.setVisible(true);
        }
    }//GEN-LAST:event_btnDetaljiActionPerformed

