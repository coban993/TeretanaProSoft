    private void popuniTabelu() {
        KlijentskiZahtev kz = new KlijentskiZahtev(operacije.Operacije.VRATI_LISTU_PLANOVA_TRENINGA, new PlanTreninga());
        komunikacija.KomunikacijaSaServerom.getInstanca().posaljiZahtev(kz);
        ServerskiOdgovor so = komunikacija.KomunikacijaSaServerom.getInstanca().primiOdgovor();
        
        ArrayList<PlanTreninga> planoviBaza = (ArrayList<PlanTreninga>) so.getOdgovor();
        ModelTabelePlanovi mtp = (ModelTabelePlanovi) tblTrening.getModel();
        mtp.popuniTabelu(planoviBaza);
    }

    private int vratiPlanTreinigaID(PlanTreninga pt) {
        KlijentskiZahtev kz = new KlijentskiZahtev(operacije.Operacije.VRATI_IZABRAN_PLAN_TRENINGA, pt);
        komunikacija.KomunikacijaSaServerom.getInstanca().posaljiZahtev(kz);
        ServerskiOdgovor so = komunikacija.KomunikacijaSaServerom.getInstanca().primiOdgovor();
        
        if (so.isUspesno()) {
            JOptionPane.showMessageDialog(this, so.getPoruka());
            return (int) so.getOdgovor();
        } else {
            JOptionPane.showMessageDialog(this, so.getPoruka(), "Greska", JOptionPane.ERROR_MESSAGE);
            return 0;
        }
    }
